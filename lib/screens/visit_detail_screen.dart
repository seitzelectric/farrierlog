              addLabel: 'Add Charge',
            ),
            if (_charges.isEmpty)
              const Padding(
                padding: EdgeInsets.all(16),
                child: Text('No travel or incidental charges yet'),
              )
            else
              ..._charges.map((charge) => Card(
                    margin:
                        const EdgeInsets.symmetric(horizontal: 16, vertical: 4),
                    child: ListTile(
                      leading: CircleAvatar(
                        backgroundColor:
                            Theme.of(context).colorScheme.secondaryContainer,
                        child: Icon(
                          charge.type.icon,
                          color:
                              Theme.of(context).colorScheme.onSecondaryContainer,
                        ),
                      ),
                      title: Text(charge.description),
                      subtitle: charge.type.isMileageBased
                          ? Text(
                              '${charge.quantity} mi @ ${AppUtils.formatCurrency(charge.rate)}/mi')
                          : null,
                      trailing: Row(
                        mainAxisSize: MainAxisSize.min,
                        children: [
                          Text(
                            AppUtils.formatCurrency(charge.total),
                            style: Theme.of(context).textTheme.titleMedium,
                          ),
                          PopupMenuButton<String>(
                            onSelected: (value) {
                              if (value == 'edit') _editCharge(charge);
                              if (value == 'delete') _deleteCharge(charge);
                            },
                            itemBuilder: (context) => const [
                              PopupMenuItem(value: 'edit', child: Text('Edit')),
                              PopupMenuItem(
                                value: 'delete',
                                child: Text('Delete',
                                    style: TextStyle(color: Colors.red)),
                              ),
                            ],
                          ),
                        ],
                      ),
                    ),
                  )),
            if (_serviceLines.isNotEmpty || _charges.isNotEmpty) ...[
              const Divider(height: 24),
              Padding(
                padding: const EdgeInsets.symmetric(horizontal: 16),
                child: Column(
                  crossAxisAlignment: CrossAxisAlignment.end,
                  children: [
                    if (_charges.isNotEmpty)
                      Padding(
                        padding: const EdgeInsets.only(bottom: 4),
                        child: Text(
                          'Services: ${AppUtils.formatCurrency(_serviceLinesTotal)} '
                          '· Travel & Incidentals: ${AppUtils.formatCurrency(_chargesTotal)}',
                          style: Theme.of(context).textTheme.bodySmall,
                        ),
                      ),
                    Row(
                      mainAxisAlignment: MainAxisAlignment.spaceBetween,
                      children: [
                        Text('Total',
                            style:
                                Theme.of(context).textTheme.titleLarge?.copyWith(
                                      fontWeight: FontWeight.bold,
                                    )),
                        Text(
                          AppUtils.formatCurrency(_total),
                          style:
                              Theme.of(context).textTheme.titleLarge?.copyWith(
                                    fontWeight: FontWeight.bold,
                                    color: _visit.paid
                                        ? Colors.green
                                        : Theme.of(context).colorScheme.primary,
                                  ),
                        ),
                      ],
                    ),
                  ],
                ),
              ),
              if (!_visit.paid && _invoices.isEmpty)
                Padding(
                  padding: const EdgeInsets.all(16),
                  child: ElevatedButton.icon(
                    onPressed: _generateInvoice,
                    icon: const Icon(Icons.receipt_long),
                    label: const Text('Generate Invoice'),
                    style: ElevatedButton.styleFrom(
                      minimumSize: const Size(double.infinity, 48),
                    ),
                  ),
                ),
            ],
            const SizedBox(height: 16),
            SectionHeader(
              title: 'Invoice History',
              onAdd: (_serviceLines.isNotEmpty || _charges.isNotEmpty) &&
                      _invoices.isEmpty
                  ? _generateInvoice
                  : null,
              addLabel: 'Create Invoice',
            ),
            if (_invoices.isEmpty)
              Padding(
                padding: const EdgeInsets.all(16),
                child: _serviceLines.isEmpty && _charges.isEmpty
                    ? const Text(
                        'Add service lines or charges before creating an invoice')
                    : ElevatedButton.icon(
                        onPressed: _generateInvoice,
                        icon: const Icon(Icons.receipt_long),
                        label: const Text('Create Invoice'),
                        style: ElevatedButton.styleFrom(
                          minimumSize: const Size(double.infinity, 48),
                        ),
                      ),
              )
            else
              ..._invoices.map(
                (invoice) => Card(
                  margin: const EdgeInsets.symmetric(
                    horizontal: 16,
                    vertical: 4,
                  ),
                  child: ListTile(
                    leading: CircleAvatar(
                      backgroundColor: invoice.paidAt != null
                          ? Colors.green.shade100
                          : Theme.of(context).colorScheme.secondaryContainer,
                      child: Icon(
                        invoice.paidAt != null
                            ? Icons.check_circle
                            : Icons.receipt_long,
                        color: invoice.paidAt != null
                            ? Colors.green
                            : Theme.of(context)
                                .colorScheme
                                .onSecondaryContainer,
                      ),
                    ),
                    title: Text(invoice.invoiceNumber),
                    subtitle: Text(
                      '${AppUtils.formatDate(invoice.issuedAt)} · ${invoice.paidAt != null ? 'Paid' : 'Unpaid'}',
                    ),
                    trailing: Row(
                      mainAxisSize: MainAxisSize.min,
                      children: [
                        Text(AppUtils.formatCurrency(invoice.total)),
                        PopupMenuButton<String>(
                          onSelected: (value) {
                            if (value == 'view') _viewInvoice(invoice);
                            if (value == 'share') _shareInvoiceRecord(invoice);
                            if (value == 'print') _printInvoiceRecord(invoice);
                          },
                          itemBuilder: (context) => const [
                            PopupMenuItem(
                              value: 'view',
                              child: Text('View'),
                            ),
                            PopupMenuItem(
                              value: 'share',
                              child: Text('Share'),
                            ),
                            PopupMenuItem(
                              value: 'print',
                              child: Text('Print'),
                            ),
                          ],
                        ),
                      ],
                    ),
                    onTap: () => _viewInvoice(invoice),
                  ),
                ),
              ),
            const SizedBox(height: 16),
            SectionHeader(
              title: 'Photos (${_photos.length})',
              onAdd: _addPhoto,
              addLabel: 'Add Photo',
            ),
            if (_photos.isEmpty)
              const Padding(
                padding: EdgeInsets.all(16),
                child: Text('No photos yet'),
              )
            else
              PhotoGrid(
                photos: _photos,
                onTap: (photo) => _showPhotoFullScreen(photo),
                onLongPress: (photo) => _deletePhoto(photo),
              ),
          ],
        ),
      ),
    );
  }
